import 'package:flutter/material.dart';

class CustomColor {
  const CustomColor();

  static const CustomColor get = CustomColor();

  Color get white => Color(0xffFFFFFF);

  Color get broken_white => Color(0xffE7E6F7);

  Color get red => Color(0xffFF222F);

  Color get light_pink => Color(0xffEA638C);

  Color get deep_pink => Color(0xffB33C86);

  Color get light_blue => Color(0xffA4BEF3);

  Color get deep_blue => Color(0xff12355B);

  Color get light_grey => Color(0xff151414);

  Color get dark_grey => Color(0xff090808);

  Color get black => Color(0xff000000);
}
